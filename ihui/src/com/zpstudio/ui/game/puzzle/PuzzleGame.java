package com.zpstudio.ui.game.puzzle;

public class PuzzleGame {
	long id;
	int counum;
	int surplusnum;
	int ongoingnum;
	int level;
	double price;
	double payment;
	int timelimit;
	String picture;
	double credit;
	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}
	/**
	 * @return the counum
	 */
	public int getCounum() {
		return counum;
	}
	/**
	 * @param counum the counum to set
	 */
	public void setCounum(int counum) {
		this.counum = counum;
	}
	/**
	 * @return the surplusnum
	 */
	public int getSurplusnum() {
		return surplusnum;
	}
	/**
	 * @param surplusnum the surplusnum to set
	 */
	public void setSurplusnum(int surplusnum) {
		this.surplusnum = surplusnum;
	}
	/**
	 * @return the ongoingnum
	 */
	public int getOngoingnum() {
		return ongoingnum;
	}
	/**
	 * @param ongoingnum the ongoingnum to set
	 */
	public void setOngoingnum(int ongoingnum) {
		this.ongoingnum = ongoingnum;
	}
	/**
	 * @return the level
	 */
	public int getLevel() {
		return level;
	}
	/**
	 * @param level the level to set
	 */
	public void setLevel(int level) {
		this.level = level;
	}
	/**
	 * @return the price
	 */
	public double getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(double price) {
		this.price = price;
	}
	/**
	 * @return the payment
	 */
	public double getPayment() {
		return payment;
	}
	/**
	 * @param payment the payment to set
	 */
	public void setPayment(double payment) {
		this.payment = payment;
	}
	/**
	 * @return the timelimit
	 */
	public int getTimelimit() {
		return timelimit;
	}
	/**
	 * @param timelimit the timelimit to set
	 */
	public void setTimelimit(int timelimit) {
		this.timelimit = timelimit;
	}
	/**
	 * @return the picture
	 */
	public String getPicture() {
		return picture;
	}
	/**
	 * @param picture the picture to set
	 */
	public void setPicture(String picture) {
		this.picture = picture;
	}
	/**
	 * @return the credit
	 */
	public double getCredit() {
		return credit;
	}
	/**
	 * @param credit the credit to set
	 */
	public void setCredit(double credit) {
		this.credit = credit;
	}
	
	
}
