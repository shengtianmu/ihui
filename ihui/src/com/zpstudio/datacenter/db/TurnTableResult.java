package com.zpstudio.datacenter.db;


public class TurnTableResult {
	double angle;
	String notifyMsg;
	double playerAvailableCredit;
	/**
	 * @return the angle
	 */
	public double getAngle() {
		return angle;
	}
	/**
	 * @param angle the angle to set
	 */
	public void setAngle(double angle) {
		this.angle = angle;
	}
	
	/**
	 * @return the notifyMsg
	 */
	public String getNotifyMsg() {
		return notifyMsg;
	}
	/**
	 * @param notifyMsg the notifyMsg to set
	 */
	public void setNotifyMsg(String notifyMsg) {
		this.notifyMsg = notifyMsg;
	}
	/**
	 * @return the playerAvailableCredit
	 */
	public double getPlayerAvailableCredit() {
		return playerAvailableCredit;
	}
	/**
	 * @param playerAvailableCredit the playerAvailableCredit to set
	 */
	public void setPlayerAvailableCredit(double playerAvailableCredit) {
		this.playerAvailableCredit = playerAvailableCredit;
	}
	
	
	
}
